# In Python, variables are dynamically typed, meaning you don't need to explicitly declare their type.
# They are inferred based on the assigned value.

# `var` declares 1 or more variables.
a = "initial"
print(a)

# You can declare multiple variables at once.
b, c = 1, 2
print(b, c)

# Python infers the type of initialized variables.
d = True
print(d)

# Variables declared without a corresponding initialization are "None".
e = None
print(e)

# The `:=` syntax in Go doesn't exist in Python. 
# In Python, you simply assign the value to declare and initialize a variable.
f = "short"
print(f)

